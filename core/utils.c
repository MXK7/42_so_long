void error(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], i);
		i++;
	}
	return ;
}

int count_line(char *str)
{
	int i;

	i = 0;
	while (str[i] == '\n')
	{
		i++;
	}
	return (i);
}